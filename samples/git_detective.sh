#!/usr/bin/env bash
# =============================================================================
# 🔍 Git 탐정 (Git Detective)
# =============================================================================
#
# 설명:
#   GitHub Copilot CLI를 활용하여 Git 히스토리를 분석하고, 버그를 도입한 커밋을
#   추적하며, 변경 이력 요약 리포트를 생성하는 예제 스크립트입니다.
#
# 사용법:
#   # 스크립트 전체를 실행하기보다는, 아래 예제를 참고하여 개별 명령어를 사용하세요.
#   ./samples/git_detective.sh
#
# 사전 요구사항:
#   - GitHub Copilot CLI 설치 (init.sh 참고)
#   - Git 저장소 내에서 실행
# =============================================================================

set -euo pipefail

echo "🔍 === Git 탐정: Copilot CLI로 Git 히스토리 분석하기 ==="
echo ""

# -----------------------------------------------------------------------------
# 📊 1단계: 핫스팟 분석 — 자주 변경되는 파일 찾기
# -----------------------------------------------------------------------------
# 자주 변경되는 파일은 버그가 숨어 있을 확률이 높습니다.
# Copilot CLI에게 최근 변경이 많았던 파일을 분석해달라고 요청합니다.

echo "📊 [1/5] 핫스팟 분석: 최근 일주일간 가장 많이 변경된 파일 찾기"
echo "────────────────────────────────────────────────────────────"
echo ""
echo '실행할 명령어:'
echo '  copilot -p "이 레포에서 최근 일주일간 가장 많이 변경된 파일 5개를 찾아줘"'
echo ""
echo "💡 Copilot은 git log를 분석해서 변경 빈도가 높은 파일 목록과"
echo "   각 파일이 왜 자주 변경되는지 인사이트를 제공합니다."
echo ""

# 참고: 동일한 분석을 수동으로 하려면 아래 Git 명령을 사용합니다
# git log --since="1 week ago" --name-only --pretty=format: | \
#   sort | uniq -c | sort -rn | head -5

# -----------------------------------------------------------------------------
# 🐛 2단계: 버그 커밋 분석 — fix 커밋에서 패턴 찾기
# -----------------------------------------------------------------------------
# 'fix' 키워드가 포함된 커밋을 분석하면 반복되는 버그 패턴을 발견할 수 있습니다.

echo "🐛 [2/5] 버그 패턴 분석: fix 커밋에서 반복되는 패턴 찾기"
echo "────────────────────────────────────────────────────────────"
echo ""
echo '실행할 명령어:'
echo '  copilot -p "최근 커밋 중 fix 키워드가 포함된 커밋을 분석해서 어떤 종류의 버그가 많았는지 요약해줘"'
echo ""
echo "💡 Copilot은 fix 커밋의 메시지와 변경 내용을 분석해서"
echo "   버그 카테고리별 통계와 재발 방지 제안을 해줍니다."
echo ""

# -----------------------------------------------------------------------------
# 👤 3단계: 기여자 분석 — 누가 어떤 영역을 담당하는가
# -----------------------------------------------------------------------------
# 코드 리뷰어를 찾거나 질문할 사람을 찾을 때 유용합니다.

echo "👤 [3/5] 기여자 분석: 모듈별 핵심 기여자 파악"
echo "────────────────────────────────────────────────────────────"
echo ""
echo '실행할 명령어:'
echo '  copilot -p "이 저장소의 각 디렉터리별로 가장 많은 커밋을 한 기여자를 정리해줘. 코드 리뷰 요청 시 누구에게 해야 할지 추천해줘"'
echo ""
echo "💡 git shortlog와 디렉터리 구조를 교차 분석하여"
echo "   영역별 전문가(code owner)를 식별합니다."
echo ""

# -----------------------------------------------------------------------------
# 🕵️ 4단계: 회귀 추적 — 특정 기능이 깨진 시점 찾기
# -----------------------------------------------------------------------------
# git bisect를 Copilot이 도와주는 시나리오입니다.

echo "🕵️ [4/5] 회귀 추적: 기능이 깨진 시점 찾기"
echo "────────────────────────────────────────────────────────────"
echo ""
echo '실행할 명령어:'
echo '  copilot -p "login 함수가 정상 동작하던 마지막 커밋을 찾아줘. git bisect를 활용해서 회귀가 발생한 커밋을 특정해줘"'
echo ""
echo "💡 Copilot은 git bisect 시작, 좋은/나쁜 커밋 마킹,"
echo "   테스트 실행까지의 전체 워크플로를 안내합니다."
echo ""

# -----------------------------------------------------------------------------
# 📝 5단계: 릴리스 노트 자동 생성
# -----------------------------------------------------------------------------
# 두 태그/커밋 사이의 변경사항을 릴리스 노트로 정리합니다.

echo "📝 [5/5] 릴리스 노트 자동 생성"
echo "────────────────────────────────────────────────────────────"
echo ""
echo '실행할 명령어:'
echo '  copilot -p "v1.0.0 태그 이후의 모든 커밋을 분석해서 카테고리별(기능 추가, 버그 수정, 성능 개선, 문서 업데이트)로 릴리스 노트를 한국어로 작성해줘"'
echo ""
echo "💡 Conventional Commits 형식을 자동 인식하고,"
echo "   사용자 친화적인 릴리스 노트를 생성합니다."
echo ""

# -----------------------------------------------------------------------------
# 🎯 보너스: 복합 분석 파이프라인
# -----------------------------------------------------------------------------
# 여러 분석을 조합하여 종합 보고서를 만드는 예제입니다.

echo "🎯 보너스: 종합 코드 건강도 리포트"
echo "────────────────────────────────────────────────────────────"
echo ""
echo '실행할 명령어:'
echo '  copilot -p "이 저장소의 코드 건강도를 종합적으로 평가해줘:'
echo '    1) 최근 한 달간 변경 빈도가 높은 핫스팟 파일'
echo '    2) 반복되는 버그 패턴'
echo '    3) 테스트 커버리지가 부족해 보이는 영역'
echo '    4) 리팩터링이 필요한 부분'
echo '    결과를 마크다운 표로 정리해줘"'
echo ""

echo "✅ Git 탐정 예제를 모두 살펴보았습니다!"
echo "   위 명령어를 복사해서 실제 저장소에서 사용해보세요."
